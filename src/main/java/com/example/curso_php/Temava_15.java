package com.example.curso_php;

public class Temava_15 {

    private  String titulo15a;
    private  String descripcion15a;
    private  String imagen15a;
    public Temava_15() {
    }


    public Temava_15(String titulo15a, String descripcion15a, String imagen15a) {
        this.titulo15a = titulo15a;
        this.descripcion15a = descripcion15a;
        this.imagen15a = imagen15a;
    }

    public String getTitulo15a() {
        return titulo15a;
    }

    public void setTitulo15a(String titulo15a) {
        this.titulo15a = titulo15a;
    }

    public String getDescripcion15a() {
        return descripcion15a;
    }

    public void setDescripcion15a(String descripcion15a) {
        this.descripcion15a = descripcion15a;
    }

    public String getImagen15a() {
        return imagen15a;
    }

    public void setImagen15a(String imagen15a) {
        this.imagen15a = imagen15a;
    }


}
