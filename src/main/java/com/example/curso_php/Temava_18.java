package com.example.curso_php;

public class Temava_18 {

    private  String titulo18a;
    private  String descripcion18a;
    private  String imagen18a;
    public Temava_18() {
    }


    public Temava_18(String titulo18a, String descripcion18a, String imagen18a) {
        this.titulo18a = titulo18a;
        this.descripcion18a = descripcion18a;
        this.imagen18a = imagen18a;
    }

    public String getTitulo18a() {
        return titulo18a;
    }

    public void setTitulo18a(String titulo18a) {
        this.titulo18a = titulo18a;
    }

    public String getDescripcion18a() {
        return descripcion18a;
    }

    public void setDescripcion18a(String descripcion18a) {
        this.descripcion18a = descripcion18a;
    }

    public String getImagen18a() {
        return imagen18a;
    }

    public void setImagen18a(String imagen18a) {
        this.imagen18a = imagen18a;
    }


}
