package com.example.curso_php;

public class Temava_16 {

    private  String titulo16a;
    private  String descripcion16a;
    private  String imagen16a;
    public Temava_16() {
    }


    public Temava_16(String titulo16a, String descripcion16a, String imagen16a) {
        this.titulo16a = titulo16a;
        this.descripcion16a = descripcion16a;
        this.imagen16a = imagen16a;
    }

    public String getTitulo16a() {
        return titulo16a;
    }

    public void setTitulo16a(String titulo16a) {
        this.titulo16a = titulo16a;
    }

    public String getDescripcion16a() {
        return descripcion16a;
    }

    public void setDescripcion16a(String descripcion16a) {
        this.descripcion16a = descripcion16a;
    }

    public String getImagen16a() {
        return imagen16a;
    }

    public void setImagen16a(String imagen16a) {
        this.imagen16a = imagen16a;
    }


}
