package curso_basico.temas;

public class Tema_10 {
    private  String titulo10;
    private  String descripcion10;
    private  String imagen10;

    public Tema_10() {
    }

    public Tema_10(String titulo10, String descripcion10, String imagen10) {
        this.titulo10 = titulo10;
        this.descripcion10 = descripcion10;
        this.imagen10 = imagen10;
    }

    public String getTitulo10() {
        return titulo10;
    }

    public void setTitulo10(String titulo10) {
        this.titulo10 = titulo10;
    }

    public String getDescripcion10() {
        return descripcion10;
    }

    public void setDescripcion10(String descripcion10) {
        this.descripcion10 = descripcion10;
    }

    public String getImagen10() {
        return imagen10;
    }

    public void setImagen10(String imagen10) {
        this.imagen10 = imagen10;
    }
}
