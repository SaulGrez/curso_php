package curso_basico.temas;

public class Tema_4 {
    private  String titulo4;
    private  String descripcion4;
    private  String imagen4;

    public Tema_4() {
    }

    public Tema_4(String titulo4, String descripcion4, String imagen4) {
        this.titulo4 = titulo4;
        this.descripcion4 = descripcion4;
        this.imagen4 = imagen4;
    }

    public String getTitulo4() {
        return titulo4;
    }

    public void setTitulo4(String titulo4) {
        this.titulo4 = titulo4;
    }

    public String getDescripcion4() {
        return descripcion4;
    }

    public void setDescripcion4(String descripcion4) {
        this.descripcion4 = descripcion4;
    }

    public String getImagen4() {
        return imagen4;
    }

    public void setImagen4(String imagen4) {
        this.imagen4 = imagen4;
    }
}
