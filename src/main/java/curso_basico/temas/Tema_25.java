package curso_basico.temas;

public class Tema_25 {
    private  String titulo25;
    private  String descripcion25;
    private  String imagen25;

    public Tema_25() {
    }

    public Tema_25(String titulo25, String descripcion25, String imagen25) {
        this.titulo25 = titulo25;
        this.descripcion25 = descripcion25;
        this.imagen25 = imagen25;
    }

    public String getTitulo25() {
        return titulo25;
    }

    public void setTitulo25(String titulo25) {
        this.titulo25 = titulo25;
    }

    public String getDescripcion25() {
        return descripcion25;
    }

    public void setDescripcion25(String descripcion25) {
        this.descripcion25 = descripcion25;
    }

    public String getImagen25() {
        return imagen25;
    }

    public void setImagen25(String imagen25) {
        this.imagen25 = imagen25;
    }
}
