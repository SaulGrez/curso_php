package curso_basico.temas;

public class Tema_21 {
    private  String titulo21;
    private  String descripcion21;
    private  String imagen21;

    public Tema_21() {
    }

    public Tema_21(String titulo21, String descripcion21, String imagen21) {
        this.titulo21 = titulo21;
        this.descripcion21 = descripcion21;
        this.imagen21 = imagen21;
    }

    public String getTitulo21() {
        return titulo21;
    }

    public void setTitulo21(String titulo21) {
        this.titulo21 = titulo21;
    }

    public String getDescripcion21() {
        return descripcion21;
    }

    public void setDescripcion21(String descripcion21) {
        this.descripcion21 = descripcion21;
    }

    public String getImagen21() {
        return imagen21;
    }

    public void setImagen21(String imagen21) {
        this.imagen21 = imagen21;
    }
}
