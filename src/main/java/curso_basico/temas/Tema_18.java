package curso_basico.temas;

public class Tema_18 {
    private String titulo18;
    private String descripcion18;
    private String imagen18;

    public Tema_18() {
    }

    public Tema_18(String titulo18, String descripcion18, String imagen18) {
        this.titulo18 = titulo18;
        this.descripcion18 = descripcion18;
        this.imagen18 = imagen18;
    }

    public String getTitulo18() {
        return titulo18;
    }

    public void setTitulo18(String titulo18) {
        this.titulo18 = titulo18;
    }

    public String getDescripcion18() {
        return descripcion18;
    }

    public void setDescripcion18(String descripcion18) {
        this.descripcion18 = descripcion18;
    }

    public String getImagen18() {
        return imagen18;
    }

    public void setImagen18(String imagen18) {
        this.imagen18 = imagen18;
    }
}