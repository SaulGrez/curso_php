package curso_basico.temas;

public class Tema_16 {
    private String titulo16;
    private String descripcion16;
    private String imagen16;

    public Tema_16() {
    }

    public Tema_16(String titulo16, String descripcion16, String imagen16) {
        this.titulo16 = titulo16;
        this.descripcion16 = descripcion16;
        this.imagen16 = imagen16;
    }

    public String getTitulo16() {
        return titulo16;
    }

    public void setTitulo16(String titulo16) {
        this.titulo16 = titulo16;
    }

    public String getDescripcion16() {
        return descripcion16;
    }

    public void setDescripcion16(String descripcion16) {
        this.descripcion16 = descripcion16;
    }

    public String getImagen16() {
        return imagen16;
    }

    public void setImagen16(String imagen16) {
        this.imagen16 = imagen16;
    }
}