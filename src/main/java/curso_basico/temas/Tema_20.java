package curso_basico.temas;

public class Tema_20 {
    private  String titulo20;
    private  String descripcion20;
    private  String imagen20;

    public Tema_20() {
    }

    public Tema_20(String titulo20, String descripcion20, String imagen20) {
        this.titulo20 = titulo20;
        this.descripcion20 = descripcion20;
        this.imagen20 = imagen20;
    }

    public String getTitulo20() {
        return titulo20;
    }

    public void setTitulo20(String titulo20) {
        this.titulo20 = titulo20;
    }

    public String getDescripcion20() {
        return descripcion20;
    }

    public void setDescripcion20(String descripcion20) {
        this.descripcion20 = descripcion20;
    }

    public String getImagen20() {
        return imagen20;
    }

    public void setImagen20(String imagen20) {
        this.imagen20 = imagen20;
    }
}
