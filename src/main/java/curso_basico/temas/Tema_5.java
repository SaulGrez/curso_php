package curso_basico.temas;

public class Tema_5 {
    private  String titulo5;
    private  String descripcion5;
    private  String imagen5;

    public Tema_5() {
    }

    public Tema_5(String titulo5, String descripcion5, String imagen5) {
        this.titulo5 = titulo5;
        this.descripcion5 = descripcion5;
        this.imagen5 = imagen5;
    }

    public String getTitulo5() {
        return titulo5;
    }

    public void setTitulo5(String titulo5) {
        this.titulo5 = titulo5;
    }

    public String getDescripcion5() {
        return descripcion5;
    }

    public void setDescripcion5(String descripcion5) {
        this.descripcion5 = descripcion5;
    }

    public String getImagen5() {
        return imagen5;
    }

    public void setImagen5(String imagen5) {
        this.imagen5 = imagen5;
    }
}
