package curso_basico.temas;

public class Tema_15 {
    private String titulo15;
    private String descripcion15;
    private String imagen15;

    public Tema_15() {
    }

    public Tema_15(String titulo15, String descripcion15, String imagen15) {
        this.titulo15 = titulo15;
        this.descripcion15 = descripcion15;
        this.imagen15 = imagen15;
    }

    public String getTitulo15() {
        return titulo15;
    }

    public void setTitulo15(String titulo15) {
        this.titulo15 = titulo15;
    }

    public String getDescripcion15() {
        return descripcion15;
    }

    public void setDescripcion15(String descripcion15) {
        this.descripcion15 = descripcion15;
    }

    public String getImagen15() {
        return imagen15;
    }

    public void setImagen15(String imagen15) {
        this.imagen15 = imagen15;
    }
}