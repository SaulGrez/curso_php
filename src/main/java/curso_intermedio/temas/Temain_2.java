package curso_intermedio.temas;

public class Temain_2 {
    private String titulo2i;
    private String descripcion2i;
    private String imagen2i;

    public Temain_2() {
    }

    public Temain_2(String titulo2i, String descripcion2i, String imagen2i) {
        this.titulo2i = titulo2i;
        this.descripcion2i = descripcion2i;
        this.imagen2i = imagen2i;
    }

    public String getTitulo2i() {
        return titulo2i;
    }

    public void setTitulo2i(String titulo2i) {
        this.titulo2i = titulo2i;
    }

    public String getDescripcion2i() {
        return descripcion2i;
    }

    public void setDescripcion2i(String descripcion2i) {
        this.descripcion2i = descripcion2i;
    }

    public String getImagen2i() {
        return imagen2i;
    }

    public void setImagen2i(String imagen2i) {
        this.imagen2i = imagen2i;
    }
}
