package curso_avanzado.temas;

public class Temava_3 {

    private  String titulo3a;
    private  String descripcion3a;
    private  String imagen3a;
    public Temava_3() {
    }


    public Temava_3(String titulo3a, String descripcion3a, String imagen3a) {
        this.titulo3a = titulo3a;
        this.descripcion3a = descripcion3a;
        this.imagen3a = imagen3a;
    }

    public String getTitulo3a() {
        return titulo3a;
    }

    public void setTitulo3a(String titulo3a) {
        this.titulo3a = titulo3a;
    }

    public String getDescripcion3a() {
        return descripcion3a;
    }

    public void setDescripcion3a(String descripcion3a) {
        this.descripcion3a = descripcion3a;
    }

    public String getImagen3a() {
        return imagen3a;
    }

    public void setImagen3a(String imagen3a) {
        this.imagen3a = imagen3a;
    }




}
