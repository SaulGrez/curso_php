package curso_avanzado.temas;

public class Temava_2 {

    private  String titulo2a;
    private  String descripcion2a;
    private  String imagen2a;
    public Temava_2() {
    }


    public Temava_2(String titulo2a, String descripcion2a, String imagen2a) {
        this.titulo2a = titulo2a;
        this.descripcion2a = descripcion2a;
        this.imagen2a = imagen2a;
    }

    public String getTitulo2a() {
        return titulo2a;
    }

    public void setTitulo2a(String titulo2a) {
        this.titulo2a = titulo2a;
    }

    public String getDescripcion2a() {
        return descripcion2a;
    }

    public void setDescripcion2a(String descripcion2a) {
        this.descripcion2a = descripcion2a;
    }

    public String getImagen2a() {
        return imagen2a;
    }

    public void setImagen2a(String imagen2a) {
        this.imagen2a = imagen2a;
    }




}
